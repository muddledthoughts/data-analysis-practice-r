#Create a vector that contains the value 4 repeated ten times using the rep() function.

#Store the result in the variable vector_1.
#Create a range of values from 1 to 12.

#Store the result in the variable vector_2.
#Create a sequence of values from 2.0 to 5.50 by 0.25.

#Store the result in the variable vector_3.

vector_1 <- rep(4, times = 10)

vector_2 <- range(1:12)

vector_3 <- seq(from = 2.0, to = 5.50, by = 0.25)

print(vector_1)
print(vector_2)
print(vector_3)

